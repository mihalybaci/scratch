# ╔═╡ 6ddaf0d0-ee62-4f36-94c6-05f3bfb18e49
function run!(in, out, c; maxiter=16)
    out .= escape.(in, c, maxiter)
end

# ╔═╡ 6341b004-4426-4e30-9015-b1ee1a75e0da
function juliaset_gpu(;width=800, height=600, maxiter=256, c = ComplexF64(-0.512511498387847167, 0.521295573094847167))
    q =  CuArray([ComplexF64(real, imag) for imag in range(-2.0, 0.5, length=width), real in range(-1.0, 1.0, length=height)])
    result = CuArray(zeros(UInt8, size(q)))
    run!(q, result, maxiter=maxiter)
    return result
end


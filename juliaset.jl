md"#### Parallelize with threads and benchmark"

# ╔═╡ add716ef-d45b-4aab-8e00-666adaf38c77
function juliaset_threads(;width=800, height=600, maxiter=256, c = ComplexF64(-0.512511498387847167, 0.521295573094847167))
	real = range(-1.5, 1.5, length=width)
	imag = range(-1, 1, length=height)*im
    result =  zeros(Int, height, width)
	Threads.@threads for x = 1:width
		for y = 1:height
			z₀ = real[x] + imag[y]
			result[y, x] = escape(z₀, c, maxiter)
		end
	end
    return result
end

# ╔═╡ 2ee833fe-31e2-4784-be9a-1a4331ee10f5
jst = @benchmark juliaset_threads()

# ╔═╡ 6ffd8442-f7fc-44bc-b3e4-a7d6ddb0fb1f
md"#### Parallelize with tasks"

# ╔═╡ b39c8c16-9162-46e7-9fbf-4400eb69dd17
function juliaset_spawn(;width=800, height=600, maxiter=256, c = ComplexF64(-0.512511498387847167, 0.521295573094847167))
	real = range(-1.5, 1.5, length=width)
	imag = range(-1, 1, length=height)*im
    result =  zeros(Int, height, width)
	@sync for x = 1:width
		Threads.@spawn for y = 1:height
			z₀ = real[x] + imag[y]
			result[y, x] = escape(z₀,c, maxiter)
		end
	end
    return result
end


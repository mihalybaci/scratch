# ╔═╡ 9da77179-daff-4eb7-b95b-1c5b9da5e524
md"#### The escape/iteration function"

# ╔═╡ 81fa04e2-147d-496d-bea4-cfe14a67868a
function escape(z0, c, maxiter)
    z = z0
    for i ∈ 1:maxiter
        abs2(z) > 4 && return (i-1) % UInt8
         z = z*z + c
    end
    return maxiter % UInt8
end

# ╔═╡ 62c83a9e-d5ba-4479-b3b4-4584bbf2b70f
@terminal @code_warntype escape(1.0+1im, 0+0.5im, 5)

# ╔═╡ c05f8497-0ab5-4dae-8dac-3df2bfd36355
md"#### Loop through each pixel of the image and count the number of escape iterations."

# ╔═╡ 1001c583-b7ee-47e0-87ac-2d4be6e8b23b
function juliaset(;width=800, height=600, maxiter=255, c = ComplexF64(-0.512511498387847167, 0.521295573094847167))
	real = range(-1.5, 1.5, length=width)
	imag = range(-1.0, 1.0, length=height)*im
    result =  zeros(Int, height, width)
	for x = 1:width
		for y = 1:height
			z₀ = real[x] + imag[y]
			result[y, x] = escape(z₀, c, maxiter)
		end
	end
    return result
end

